    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        if(txtId.getText().equals("")){
            JTextField args[] = {txtProduto, txtMarca, txtPreco, txtCor};
            Limpador.desativaform(args);
            txtDescricao.setEditable(false);
            cbTensao.setEnabled(false);
            Limpador.limpaform(args);
            txtDescricao.setText("");
            JLabel argsl[] = {lblNome, lblCor, lblDesc, lblPreco, lblMarca, lblTensao};
            Limpador.corform(argsl);
            JOptionPane.showMessageDialog(null, "Digite algum ID.");
        }else{
            JLabel argsl[] = {lblNome, lblCor, lblDesc, lblPreco, lblMarca, lblTensao};
            Limpador.voltacorform(argsl);
            JLabel args2[] = {lblId};
            Limpador.corform(args2);
            JTextField args[] = {txtProduto, txtMarca, txtPreco, txtCor};
            Limpador.ativaform(args);
            cbTensao.setEnabled(true);
            txtDescricao.setEditable(true);
            Eletrodomesticos produtos = new Eletrodomesticos();
            produtos.setId(Integer.parseInt(txtId.getText()));
            String id = String.valueOf(produtos.getId());
            produtos.VerificarID(id, args, txtDescricao, cbTensao);
            txtId.setEditable(false);
            txtProduto.requestFocus();

        }
    }//GEN-LAST:event_btnSearchActionPerformed


