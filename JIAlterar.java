    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if(cbTensao.getModel().getSelectedItem().equals("") || txtProduto.getText().equals("") || txtMarca.getText().equals("") || txtPreco.getText().equals("") || txtCor.getText().equals("") || txtDescricao.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos.");
        }else{
            Eletrodomesticos produto = new Eletrodomesticos();
            produto.setId(Integer.parseInt(txtId.getText()));
            produto.setNome(txtProduto.getText());
            produto.setMarca(txtMarca.getText());
            produto.setPreco(Double.valueOf(txtPreco.getText().replaceAll(",",".")));
            produto.setCor(txtCor.getText());
            produto.setDescricao(txtDescricao.getText());
            produto.setTensao(cbTensao.getSelectedItem().toString());
            
            produto.Alterar(String.valueOf(produto.getId()), produto.getNome(), produto.getMarca(), String.valueOf(produto.getPreco()), produto.getCor(), produto.getDescricao(), produto.getTensao());

            //produto.Alterar(txtId.getText(), txtProduto.getText(), txtMarca.getText(), txtPreco.getText(), txtCor.getText(), txtDescricao.getText(), cbTensao.getSelectedItem().toString());
            
            JTextField s[] = {txtProduto, txtMarca, txtPreco, txtCor, txtId};
            Limpador.limpaform(s);
            txtDescricao.setText("");
            txtId.requestFocus();
            JTextField args[] = {txtProduto, txtMarca, txtPreco, txtCor};
            Limpador.desativaform(args);
            txtDescricao.setEditable(false);
            JLabel abc[] = {lblNome, lblCor, lblDesc, lblPreco, lblMarca, lblTensao};
            Limpador.corform(abc);
            cbTensao.setSelectedIndex(-1);
            txtId.setEditable(true);
            JLabel args2[] = {lblId};
            Limpador.voltacorform(args2);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

